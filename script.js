function one() {
  let cite = document.getElementById('one');
  if (cite.style.display === 'block') {
    cite.style.display = 'none';
    event.target.innerHTML = 'Click to show citation';
  } else {
      cite.style.display = 'block';
      event.target.innerHTML = 'Click to hide citation';
  }
}

